///////////////////////////////////////////////////////////////////////////
//  Function: generateSavingsCSV
//  Inputs: input - The prompt to send to the AI to generate the CSV data
//  Outputs: String - The raw CSV text generated by the AI
//  Error Checking: None
//  Summary: Sends a prompt to the Groq API to generate a CSV file based on 
// the input, and returns the raw CSV text. The function uses the fetch API 
// to make a POST request to the Groq API endpoint, including the necessary 
// headers and body with the prompt. It then processes the response to 
// extract and return the CSV content.
///////////////////////////////////////////////////////////////////////////

async function generateSavingsCSV(input) {
  const apiKey = "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";

  const prompt = input;

  const response = await fetch("https://api.groq.com/openai/v1/chat/completions", {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      "Authorization": `Bearer ${apiKey}`
    },
    body: JSON.stringify({
      model: "llama-3.1-8b-instant",
      messages: [
        { role: "user", content: prompt }
      ]
    })
  });

  const data = await response.json();
  console.log("Groq CSV response:", data);

  if (!data.choices) {
    throw new Error("Groq error: " + JSON.stringify(data, null, 2));
  }

  const csv = data.choices[0].message.content.trim();
  return csv;
}


///////////////////////////////////////////////////////////////////////////
//  Function: readCSV
//  Inputs: url - File path/URL to CSV file
//  Outputs: Array of objects read from the CSV file
//  Error Checking: None
//  Summary: Reads a CSV file from the specified URL and returns an array 
//     of objects, where each object represents a row in the CSV file with 
// keys corresponding to the column headers.
///////////////////////////////////////////////////////////////////////////
async function readCSV(url) {
  // Read from file
  const res = await fetch(url);
  const text = await res.text();

  // Add rows for each line, split by commas
  const lines = text.trim().split("\n");
  // Get headers from first line
  // const headers = lines[0].split(",");
  const headers = lines[0]
  .split(",")
  .map(h => h.replace(/\r/g, "").trim());

  // Create an array with objects for each line, with keys from headers and values from the line
  return lines.slice(1).map(line => {
    const values = line.split(",");
    const obj = {};
    headers.forEach((h, i) => {
      obj[h] = values[i];
    });
    return obj;
  });
}


///////////////////////////////////////////////////////////////////////////
//  Function: readCSVFromAI
//  Inputs: url - The prompt to send to the AI to generate the CSV data
//  Outputs: Array of objects read from the AI generated CSV file
//  Error Checking: None
//  Summary: Sends a prompt to the Groq API to generate a CSV file based on 
// the input, reads the raw CSV text, and returns an array of objects 
// representing the rows in the CSV file. Each object has keys 
// corresponding to the column headers and values corresponding to the data 
// in each row.
///////////////////////////////////////////////////////////////////////////

async function readCSVFromAI(input) {
  const csvText = await generateSavingsCSV(input);

  const lines = csvText.trim().split("\n");

  // Hardcode the expected headers instead of reading from AI output
  const headers = ["id", "Bank", "Account Name", "Interest (Decimal)", "Compoundings per Year", "Monthly Fees", "Minimum Deposit"];

  // Check if first line looks like a header row and skip it if so
  const firstLine = lines[0].toLowerCase();
  const dataLines = firstLine.includes("bank") ? lines.slice(1) : lines;

  return dataLines.map(line => {
    const values = line.split(",");
    const obj = {};
    headers.forEach((h, i) => {
      obj[h] = values[i]?.trim();
    });
    return obj;
  });
}

