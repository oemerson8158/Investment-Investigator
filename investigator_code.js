///////////////////////////////////////////////////////////////////////////
//  Function: findMax
//  Inputs: principal (number), time (number)
//  Outputs: Returns the highest final value among all filtered accounts
//  Summary: Iterates through either the filtered standard accounts or CD
//    accounts—depending on the fixed‑rate setting—and computes each
//    account’s final value using compound‑interest growth. Tracks the
//    account with the highest return and updates the global final account
//    variables (bank, account name, rate, compounding, fees, minimum
//    deposit). Returns the rounded maximum value for use in account
//    recommendations.
///////////////////////////////////////////////////////////////////////////
function findMax(principal, time) {
  let i;
  let max = 0;
  let temp;
  if (fixedRatesStatus == false) {
    for (i = 0; i < filteredStandardAccountsBankNames.length; i++) {
      temp = (principal * (Math.pow(1 + (filteredStandardAccountsBankRates[i] / filteredStandardAccountsBankCompoundings[i]), (filteredStandardAccountsBankCompoundings[i] * time))));
      if (temp > max) {
        max = temp;
        finalBank = filteredStandardAccountsBankNames[i];
        finalAccount = filteredStandardAccountsBankAccounts[i];
        finalRate = filteredStandardAccountsBankRates[i];
        finalCompounding = filteredStandardAccountsBankCompoundings[i];
        finalFee = filteredStandardAccountsBankFees[i];
        finalMinimumDeposit = filteredStandardAccountsMinimumDeposits[i];
      }
    }
    return Math.round(max);
  } else if (fixedRatesStatus == true) {
    for (i = 0; i < filteredCDAccountsBankNames.length; i++) {
      temp = (principal * (Math.pow(1 + (filteredCDAccountsBankRates[i] / filteredCDAccountsBankCompoundings[i]), (filteredCDAccountsBankCompoundings[i] * time))));
      if (temp > max) {
        max = temp;
        finalBank = filteredCDAccountsBankNames[i];
        finalAccount = filteredCDAccountsBankAccounts[i];
        finalRate = filteredCDAccountsBankRates[i];
        finalCompounding = filteredCDAccountsBankCompoundings[i];
        finalFee = filteredCDAccountsBankFees[i];
        finalMinimumDeposit = filteredCDAccountsMinimumDeposits[i];
      }
    }
    return Math.round(max);
  }
}

///////////////////////////////////////////////////////////////////////////
//  Function: sortList
//  Inputs: standardList (array), cDlist (array), condition (value to match)
//  Outputs: Updates the filtered account arrays based on the given condition
//  Summary: Filters either the standard‑account lists or CD‑account lists
//    depending on the fixed‑rate setting. It checks each account’s
//    corresponding filter list for a matching condition, and when matched,
//    copies that account’s bank name, account name, rate, compounding,
//    fees, and minimum deposit into temporary arrays. After processing,
//    it replaces the global filtered arrays with the narrowed‑down results,
//    effectively shrinking the active account list for further filtering.
///////////////////////////////////////////////////////////////////////////
function sortList(standardList, cDlist, condition) {
  let bankTemp = [];
  let accountTemp = [];
  let rateTemp = [];
  let compoundingTemp = [];
  let feeTemp = [];
  let depositTemp = [];
  if (fixedRatesStatus == false) {
    for (i = 0; i < filteredStandardAccountsBankNames.length; i++) {
      if (standardList[i] == condition) {
        appendItem(bankTemp, filteredStandardAccountsBankNames[i]);
        appendItem(accountTemp, filteredStandardAccountsBankAccounts[i]);
        appendItem(rateTemp, filteredStandardAccountsBankRates[i]);
        appendItem(compoundingTemp, filteredStandardAccountsBankCompoundings[i]);
        appendItem(feeTemp, filteredStandardAccountsBankFees[i]);
        appendItem(depositTemp, filteredStandardAccountsMinimumDeposits[i]);
      }
    }
    filteredStandardAccountsBankNames = bankTemp;
    filteredStandardAccountsBankAccounts = accountTemp;
    filteredStandardAccountsBankRates = rateTemp;
    filteredStandardAccountsBankCompoundings = compoundingTemp;
    filteredStandardAccountsBankFees = feeTemp;
    filteredStandardAccountsMinimumDeposits = depositTemp;
    return;
  } else if (fixedRatesStatus == true) {
    for (i = 0; i < filteredCDAccountsBankNames.length; i++) {
      if (cDlist[i] == condition) {
        appendItem(bankTemp, filteredCDAccountsBankNames[i]);
        appendItem(accountTemp, filteredCDAccountsBankAccounts[i]);
        appendItem(rateTemp, filteredCDAccountsBankRates[i]);
        appendItem(compoundingTemp, filteredCDAccountsBankCompoundings[i]);
        appendItem(feeTemp, filteredCDAccountsBankFees[i]);
        appendItem(depositTemp, filteredCDAccountsMinimumDeposits[i]);
      }
    }
    filteredCDAccountsBankNames = bankTemp;
    filteredCDAccountsBankAccounts = accountTemp;
    filteredCDAccountsBankRates = rateTemp;
    filteredCDAccountsBankCompoundings = compoundingTemp;
    filteredCDAccountsBankFees = feeTemp;
    filteredCDAccountsMinimumDeposits = depositTemp;
    return;
  }
}

///////////////////////////////////////////////////////////////////////////
//  Function: monthlyFeesFilter
//  Inputs: type (the ID of the user’s Yes/No dropdown)
//  Outputs: Optionally filters account lists based on monthly fees
//  Summary: Checks whether the user wants to filter out accounts with
//    monthly fees. If the user selects “Yes,” no filtering is applied.
//    If the user selects “No,” the function calls sortList to keep only
//    accounts whose monthly fee is zero, narrowing the active account
//    lists for further processing.
///////////////////////////////////////////////////////////////////////////
function monthlyFeesFilter(type) {
  let i;
  if (getText(type) == "Yes") {
    return;
  } else if (getText(type) == "No") {
    sortList(filteredStandardAccountsBankFees, filteredCDAccountsBankFees, 0);
  }
}

///////////////////////////////////////////////////////////////////////////
//  Function: minimumDepositFilter
//  Inputs: type (the ID of the user’s Yes/No dropdown)
//  Outputs: Optionally filters account lists based on minimum deposit
//  Summary: Determines whether the user wants to filter out accounts
//    requiring a minimum deposit. If the user selects “Yes,” the lists
//    remain unchanged. If the user selects “No,” the function calls
//    sortList to keep only accounts with a minimum deposit of zero,
//    reducing the filtered account lists for subsequent steps.
///////////////////////////////////////////////////////////////////////////
function minimumDepositFilter(type) {
  let i;
  if (getText(type) == "Yes") {
    return;
  } else if (getText(type) == "No") {
    sortList(filteredStandardAccountsMinimumDeposits, filteredCDAccountsMinimumDeposits, 0);
  }
}

///////////////////////////////////////////////////////////////////////////
//  Function: bankFilter
//  Inputs: input (the ID of the user’s preferred‑bank text field)
//  Outputs: Updates filtered account lists or displays an error message
//  Summary: Reads the user’s preferred bank name and checks whether it
//    exists in the filtered account lists using checkBankInput. If the
//    user selects “None,” all banks remain eligible. If a valid bank is
//    found, the function calls sortList to keep only accounts from that
//    bank. If no match is found, it notifies the user by replacing the
//    input text with “Bank Not Found.”
///////////////////////////////////////////////////////////////////////////
function bankFilter(input) {
  let i;
  checkBankInput(getText(input));
  if (getText(input) == "None") {
    bankFound = true;
    return;
  } else if (bankFound == true) {
    sortList(filteredStandardAccountsBankNames, filteredCDAccountsBankNames, getText(input));
  } else {
    setText(input, "Bank Not Found");
  }
}

