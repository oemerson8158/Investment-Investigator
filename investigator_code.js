///////////////////////////////////////////////////////////////////////////
//  Function: compareTwoAccountsGrowth
//  Inputs: None
//  Outputs: Draws a dual‑line growth graph on the Compare Rates canvas
//  Summary: Reads two user‑entered account names, resolves each one using
//    the app’s fuzzy search system, and extracts their interest rate and
//    compounding information. It then computes compound‑interest growth
//    curves for both accounts over the user‑selected principal and time
//    period. Finally, it displays the results together on a dual graph so
//    users can visually compare long‑term performance.
///////////////////////////////////////////////////////////////////////////
function compareTwoAccountsGrowth() {
  // Read user inputs
  const input1 = "compareRatesAccount1Input";
  const input2 = "compareRatesAccount2Input";

  const text1 = getText(input1).trim();
  const text2 = getText(input2).trim();

  if (!text1 || !text2) {
    console.warn("Both account inputs are required.");
    return;
  }



  function resolveAccount(inputId) {
    const result = findBestMatch(inputId);  
    if (!result) return null;

    const { index, isCD } = result;

    if (isCD) {
      return {
        rate: cDAccountsBankRates[index],
        comp: cDAccountsBankCompoundings[index],
        bank: cDAccountsBankNames[index],
        account: cDAccountsBankAccounts[index]
      };
    } else {
      return {
        rate: standardAccountsBankRates[index],
        comp: standardAccountsBankCompoundings[index],
        bank: standardAccountsBankNames[index],
        account: standardAccountsBankAccounts[index]
      };
    }
  }

  const acc1 = resolveAccount(input1);
  const acc2 = resolveAccount(input2);

  if (!acc1 || !acc2) {
    console.warn("One or both accounts could not be resolved.");
    return;
  }

  const principal = getNumber("compareRatesPrincipalInput");   
  const years = getNumber("compareRatesLengthInput");  

  const pointsA = computeGrowth(principal, acc1.rate, acc1.comp, years);
  const pointsB = computeGrowth(principal, acc2.rate, acc2.comp, years);

  drawDualGraph(pointsA, pointsB, acc1.account, acc2.account);

}

///////////////////////////////////////////////////////////////////////////
//  Function: explainCompareRates
//  Inputs: None
//  Outputs: Writes a textual explanation to the Compare Rates output area
//  Summary: Reads two user‑entered account names, resolves each one using
//    the app’s fuzzy search system, and extracts their bank, rate, and
//    compounding information. It then computes each account’s compound‑
//    interest growth over the user‑selected principal and time period,
//    compares their final values, and generates a written explanation
//    indicating which account performs better and why.
///////////////////////////////////////////////////////////////////////////
function explainCompareRates() {
  const input1 = "compareRatesAccount1Input";
  const input2 = "compareRatesAccount2Input";

  const text1 = getText(input1).trim();
  const text2 = getText(input2).trim();

  if (!text1 || !text2) {
    setText("compareRatesOutput", "Please enter two account names to compare.");
    return;
  }

  const acc1 = findBestMatch(input1);
  const acc2 = findBestMatch(input2);

  if (!acc1 || !acc2) {
    setText("compareRatesOutput", "One or both accounts could not be found.");
    return;
  }

  const a = acc1.isCD
    ? {
        bank: cDAccountsBankNames[acc1.index],
        account: cDAccountsBankAccounts[acc1.index],
        rate: cDAccountsBankRates[acc1.index],
        comp: cDAccountsBankCompoundings[acc1.index]
      }
    : {
        bank: standardAccountsBankNames[acc1.index],
        account: standardAccountsBankAccounts[acc1.index],
        rate: standardAccountsBankRates[acc1.index],
        comp: standardAccountsBankCompoundings[acc1.index]
      };

  const b = acc2.isCD
    ? {
        bank: cDAccountsBankNames[acc2.index],
        account: cDAccountsBankAccounts[acc2.index],
        rate: cDAccountsBankRates[acc2.index],
        comp: cDAccountsBankCompoundings[acc2.index]
      }
    : {
        bank: standardAccountsBankNames[acc2.index],
        account: standardAccountsBankAccounts[acc2.index],
        rate: standardAccountsBankRates[acc2.index],
        comp: standardAccountsBankCompoundings[acc2.index]
      };

  const principal = getNumber("compareRatesPrincipalInput");   
  const years = getNumber("compareRatesLengthInput"); 

  const pointsA = computeGrowth(principal, a.rate, a.comp, years);
  const pointsB = computeGrowth(principal, b.rate, b.comp, years);

  const finalA = pointsA[pointsA.length - 1];
  const finalB = pointsB[pointsB.length - 1];

  let message = "";

  if (finalA > finalB) {
    message =
      `Investment Investigator suggests ${a.account} at ${a.bank} because it ends with $${finalA.toFixed(0)}, ` +
      `which is higher than ${b.account} at ${b.bank}, ` +
      `which ends with $${finalB.toFixed(0)}.\n\n` +
      `Therefore, **${a.account}** is the better choice over a ${years}-year period with a principal of $${principal}.`;
  } else if (finalB > finalA) {
    message =
      `Investment Investigator suggests ${b.account} at ${b.bank} because it ends with $${finalB.toFixed(0)}, ` +
      `which is higher than ${a.account} at ${a.bank}, ` +
      `which ends with $${finalA.toFixed(0)}.\n\n` +
      `Therefore, **${b.account}** is the better choice over a ${years}-year period with a principal of $${principal}.`;
  } else {
    message =
      `Both accounts end with the same value of $${finalA.toFixed(0)} after ${years} years.`;
  }

  setText("compareRatesOutput", message);
}


