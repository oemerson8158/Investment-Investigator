///////////////////////////////////////////////////////////////////////////
//  Function: Find Closest Bank
//  Inputs: None
//  Outputs: The name of the bank that matches the user input most closely, 
// or null if no match is found
//  Summary: Finds the bank name in the lists that best matches the user 
// input in the search bar, using a combination of substring matching, 
// word-level matching, and Levenshtein distance for fuzzy matching. 
// Returns the name of the closest matching bank, or null if no match is found.
///////////////////////////////////////////////////////////////////////////
function findClosestBank(input) {
  const userInput = document.getElementById(input).value.trim().toLowerCase();
  if (!userInput) return null;

  const allBanks = [...standardAccountsBankNames, ...cDAccountsBankNames];

  let bestMatch = null;
  let bestScore = -Infinity;

  for (const bank of allBanks) {
    const lower = bank.toLowerCase();
    let score = 0;

    // Substring priority
    if (lower.includes(userInput)) score += 1000;

    // Word-level matching
    const inputWords = userInput.split(" ");
    const bankWords = lower.split(" ");
    for (const iw of inputWords) {
      for (const bw of bankWords) {
        if (bw === iw) score += 150;
        else if (bw.startsWith(iw)) score += 50;
      }
    }

    // Levenshtein fallback
    score -= levenshtein(userInput, lower);

    if (score > bestScore) {
      bestScore = score;
      bestMatch = bank;
    }
  }

  return bestMatch;
}

///////////////////////////////////////////////////////////////////////////
//  Function: Find Closest Account for Bank
//  Inputs: bankName - The output of findClosestBank
//  Outputs: The name of the account that matches the user input most closely, 
// or null if no match is found. Uses only accounts that are from the bank 
// outputted by findClosestBank.
//  Summary: Finds the account name in the lists that best matches the user 
// input in the search bar, using a combination of substring matching, 
// word-level matching, and Levenshtein distance for fuzzy matching.
///////////////////////////////////////////////////////////////////////////
function findClosestAccountForBank(bankName, input) {
  const userInput = getText(input);

  // Build restricted list
  const accounts = [];

  // Standard accounts
  for (let i = 0; i < standardAccountsBankNames.length; i++) {
    if (standardAccountsBankNames[i] === bankName) {
      accounts.push({
        name: standardAccountsBankAccounts[i],
        index: i,
        isCD: false
      });
    }
  }

  // CD accounts
  for (let i = 0; i < cDAccountsBankNames.length; i++) {
    if (cDAccountsBankNames[i] === bankName) {
      accounts.push({
        name: cDAccountsBankAccounts[i],
        index: i,
        isCD: true
      });
    }
  }

  // Fuzzy match within this bank’s accounts
  let best = null;
  let bestScore = -Infinity;

  for (const acc of accounts) {
    const lower = acc.name.toLowerCase();
    let score = 0;

    if (lower.includes(userInput)) score += 1000;

    const inputWords = userInput.split(" ");
    const accWords = lower.split(" ");
    for (const iw of inputWords) {
      for (const aw of accWords) {
        if (aw === iw) score += 150;
        else if (aw.startsWith(iw)) score += 50;
      }
    }

    score -= levenshtein(userInput, lower);

    if (score > bestScore) {
      bestScore = score;
      best = acc;
    }
  }

  return best; // { name, index, isCD }
}

///////////////////////////////////////////////////////////////////////////
//  Function: Find Best Match
//  Inputs: None
//  Outputs: The index and type (CD or standard) of the account that matches 
// the user input most closely, or null if no match is found. Uses only 
// accounts that are from the bank outputted by findClosestBank.
//  Summary: Finds the account that best matches the user input in the search bar.
///////////////////////////////////////////////////////////////////////////
function findBestMatch(input) {
  const bank = findClosestBank(input);
  if (!bank) return null;

  return findClosestAccountForBank(bank, input);
}


